# Old MacDonald Had a Farm - Nursery Rhyme

def old_macdonald_verse(animal, sound):
    print("Old MacDonald had a farm")
    print("Ee i ee i o")
    print(f"And on his farm he had some {animal}s")
    print("Ee i ee i o")
    print(f"With a {sound}-{sound} here")
    print(f"And a {sound}-{sound} there")
    print(f"Here a {sound}, there a {sound}")
    print(f"Everywhere a {sound}-{sound}")
    print("Old MacDonald had a farm")
    print("Ee i ee i o\n")

# Verses
old_macdonald_verse("cow", "moo")
old_macdonald_verse("chick", "cluck")
old_macdonald_verse("pig", "oink")
